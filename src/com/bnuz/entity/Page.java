package com.bnuz.entity;

public class Page {
	private int pid;
	private String titlepage;
	private String contentpage;
	private String bgroundpage;
	private String modelpage;
	private Imagepage imagepage;
	private PPT ppt;
	
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	
	public String getTitlepage() {
		return titlepage;
	}
	public void setTitlepage(String titlepage) {
		this.titlepage = titlepage;
	}
	public String getContentpage() {
		return contentpage;
	}
	public void setContentpage(String contentpage) {
		this.contentpage = contentpage;
	}
	public String getBgroundpage() {
		return bgroundpage;
	}
	public void setBgroundpage(String bgroundpage) {
		this.bgroundpage = bgroundpage;
	}
	public String getModelpage() {
		return modelpage;
	}
	public void setModelpage(String modelpage) {
		this.modelpage = modelpage;
	}
	public PPT getPpt() {
		return ppt;
	}
	public void setPpt(PPT ppt) {
		this.ppt = ppt;
	}
	public Imagepage getImagepage() {
		return imagepage;
	}
	public void setImagepage(Imagepage imagepage) {
		this.imagepage = imagepage;
	}
	
		
}
